  klee_assume( ictx.locs[2][1] == 1 );    // Occupied
  klee_assume( ictx.orients[2][1] == 1 ); // East
  //klee_assume( ictx.sigs[2][1][0] == 0 ); // Left Signal
  //klee_assume( ictx.sigs[2][1][1] == 1 ); // Right Signal
  klee_assume( ictx.sigs[2][1][0] == 0 || ictx.sigs[2][1][0] == 1 );
  klee_assume( ictx.sigs[2][1][1] == 0 || ictx.sigs[2][1][1] == 1 );
  klee_assume( !(ictx.sigs[2][1][0] == 1 && ictx.sigs[2][1][1] == 1) );

  klee_assume( ictx.locs[2][2] == 0 );    // Empty
  klee_assume( ictx.orients[2][2] == 0 ); // North
  klee_assume( ictx.sigs[2][2][0] == 0 ); // Left Signal
  klee_assume( ictx.sigs[2][2][1] == 0 ); // Right Signal

  klee_assume( ictx.locs[2][3] == 0 );    // Empty
  klee_assume( ictx.orients[2][3] == 0 ); // North
  klee_assume( ictx.sigs[2][3][0] == 0 ); // Left Signal
  klee_assume( ictx.sigs[2][3][1] == 0 ); // Right Signal

  klee_assume( ictx.locs[3][0] == 0 );    // Empty
  klee_assume( ictx.orients[3][0] == 0 ); // North
  klee_assume( ictx.sigs[3][0][0] == 0 ); // Left Signal
  klee_assume( ictx.sigs[3][0][1] == 0 ); // Right Signal

  klee_assume( ictx.locs[3][1] == 0 );    // Empty
  klee_assume( ictx.orients[3][1] == 0 ); // North
  klee_assume( ictx.sigs[3][1][0] == 0 ); // Left Signal
  klee_assume( ictx.sigs[3][1][1] == 0 ); // Right Signal

  klee_assume( ictx.locs[3][2] == 0 );    // Empty
  klee_assume( ictx.orients[3][2] == 0 ); // North
  klee_assume( ictx.sigs[3][2][0] == 0 ); // Left Signal
  klee_assume( ictx.sigs[3][2][1] == 0 ); // Right Signal

  klee_assume( ictx.locs[3][3] == 0 );    // Empty
  klee_assume( ictx.orients[3][3] == 0 ); // North
  klee_assume( ictx.sigs[3][3][0] == 0 ); // Left Signal
  klee_assume( ictx.sigs[3][3][1] == 0 ); // Right Signal

  d.row = nctx.row;
  d.col = nctx.col;

  m = imove( &l, &nctx, &ictx, &d );

  klee_assume(     m == __soid__m );
  klee_assume( d.row == __soid__d.row );
  klee_assume( d.col == __soid__d.col );

  return 0;
